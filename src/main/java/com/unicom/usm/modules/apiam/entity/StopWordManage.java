package com.unicom.usm.modules.apiam.entity;

import java.util.Date;

import com.unicom.usm.common.persistence.DataEntity;

/**
 * 
 * @author vision
 * 停用词管理
 */
public class StopWordManage extends DataEntity<StopWordManage>{

	private static final long serialVersionUID = -2033629304465123506L;
	
	private String stopWord;
	private Date createTime;
	
	public StopWordManage() {
		super();
	}
	public StopWordManage(String id) {
		super(id);
	}
	
	public String getStopWord() {
		return stopWord;
	}
	public void setStopWord(String stopWord) {
		this.stopWord = stopWord;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	@Override
	public String toString() {
		return "StopWordManage [stopWord=" + stopWord + ", createTime="
				+ createTime + "]";
	}
	
	
}
